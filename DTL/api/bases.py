
#------------------------------------------------------------
#------------------------------------------------------------
class BaseStruct(object):
    #------------------------------------------------------------
    def __init__(self):
        pass
    
    #------------------------------------------------------------
    def __str__(self):
        return '{0}({1})'.format(type(self).__name__, super(BaseStruct, self).__str__())
    
    #------------------------------------------------------------
    def __repr__(self):
        return '{0}({1})'.format(type(self).__name__, super(BaseStruct, self).__repr__())
